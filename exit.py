def exit_app():
    """This function prints goodbye and exits
    """
    print("Goodbye!")
    exit()


def display_help():
    """This function welcomes the user to the help section and prints information to assist the user
    """
    print("Welcome to the Help Section!")
    print("You can use this help command to get assistance on using this program.")


if __name__ == '__main__':
    exit_app()
    display_help()